/* ============================================================
   KPI CALCULATIONS
============================================================ */

function calculateKPIs(data) {

    let totalTasks = 0;
    let completedTasks = 0;

    const rooms = new Set();
    const staff = new Set();

    data.forEach(record => {

        if (record.room)
            rooms.add(record.room);

        if (record.staff)
            staff.add(record.staff);

        Object.values(record.tasks_completed || {}).forEach(task => {

            totalTasks++;

            if (task === "Y")
                completedTasks++;

        });

    });

    return {

        totalSurveys: data.length,

        totalRooms: rooms.size,

        totalStaff: staff.size,

        compliance:
            totalTasks === 0
                ? 0
                : Math.round(
                    completedTasks /
                    totalTasks *
                    100
                )

    };

}

