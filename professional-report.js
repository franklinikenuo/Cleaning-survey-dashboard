/* ============================================================
   ROOM STATISTICS
============================================================ */

function getRoomStatistics(data) {

    const rooms = {};

    data.forEach(record => {

        if (!record.room) return;

        if (!rooms[record.room]) {

            rooms[record.room] = {
                room: record.room,
                surveys: 0,
                yes: 0,
                total: 0
            };

        }

        rooms[record.room].surveys++;

        Object.values(record.tasks_completed || {}).forEach(task => {

            rooms[record.room].total++;

            if (task === "Y") {
                rooms[record.room].yes++;
            }

        });

    });

    return Object.values(rooms)
        .map(room => ({
            ...room,
            compliance: room.total
                ? Math.round((room.yes / room.total) * 100)
                : 0
        }))
        .sort((a, b) => b.compliance - a.compliance);

}

/* ============================================================
   STAFF STATISTICS
============================================================ */

function getStaffStatistics(data) {

    const staff = {};

    data.forEach(record => {

        if (!record.staff) return;

        record.staff
            .split(",")
            .map(name => name.trim())
            .forEach(name => {

                if (!staff[name]) {

                    staff[name] = {
                        name,
                        surveys: 0,
                        yes: 0,
                        total: 0
                    };

                }

                staff[name].surveys++;

                Object.values(record.tasks_completed || {}).forEach(task => {

                    staff[name].total++;

                    if (task === "Y") {
                        staff[name].yes++;
                    }

                });

            });

    });

    return Object.values(staff)
        .map(person => ({
            ...person,
            compliance: person.total
                ? Math.round((person.yes / person.total) * 100)
                : 0
        }))
        .sort((a, b) => b.compliance - a.compliance);

}

/* ============================================================
   SHIFT STATISTICS
============================================================ */

function getShiftStatistics(data) {

    const shifts = {

        Morning: 0,
        Afternoon: 0,
        Evening: 0,
        Night: 0

    };

    data.forEach(record => {

        if (record.shift && shifts.hasOwnProperty(record.shift)) {
            shifts[record.shift]++;
        }

    });

    return shifts;

}

